import math
# Python program to compute Surface Area of Tetrahedron
print("Surface Area of Tetrahedron")

# input / assignment
side = float(input('Side of Tetrahedron: '))

# compute / process
area = math.sqrt(3) * side**2

# output / results

print("Side =",side,"m")
print("\nSurface Area of Tetrahedron =",area,"sq.m")
